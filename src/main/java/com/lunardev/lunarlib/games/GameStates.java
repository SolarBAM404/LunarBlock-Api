package com.lunardev.lunarlib.games;

/**
 * The State enum class represents the different states of the game,
 * such as 'WAITING', 'STARTED', and 'ENDING', allowing for easy management and control of game flow.
 */
public enum GameStates {

    WAITING,
    STARTING,
    STARTED,
    PLAYING,
    ENDING,
    ENDED

}
